package com.play.edit_dp_and_post.editpost_dp;

public class GradiantColor
{
   private int fcolor;
   private int scolor;

    public GradiantColor() {
    }

    public GradiantColor(int fcolor, int scolor) {
        this.fcolor = fcolor;
        this.scolor = scolor;
    }

    public int getFcolor() {
        return fcolor;
    }

    public void setFcolor(int fcolor) {
        this.fcolor = fcolor;
    }

    public int getScolor() {
        return scolor;
    }

    public void setScolor(int scolor) {
        this.scolor = scolor;
    }
}
